# mkdir: Criar diretórios.
mkdir nome_diretorio

# rmdir: Remover diretórios vazios.
rmdir nome_diretorio

# touch: Criar arquivos vazios.
touch nome_arquivo

# cp: Copiar arquivos ou diretórios.
cp arquivo_origem arquivo_destino
cp -r diretorio_origem diretorio_destino

# mv: Mover ou renomear arquivos ou diretórios.
mv arquivo_origem arquivo_destino
mv nome_antigo novo_nome

# rm: Remover arquivos ou diretórios.
rm nome_arquivo
rm -r nome_diretorio



